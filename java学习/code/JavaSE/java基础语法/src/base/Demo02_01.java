package base;

public class Demo02_01 {
    public static void main(String[] args) {
        int i1 = 10; //十进制
        int i2 = 010; // 八进制
        int i3 = 0b10; // 二进制
        int i4 = 0x10; // 十六进制

        System.out.println(i1);
        System.out.println(i2);
        System.out.println(i3);
        System.out.println(i4);
    }
}
